const mongoose = require('mongoose');

const UserProfileSchema = new mongoose.Schema({
  userId: {
    type: String,
    required: true,
    unique: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
  },
  address: {
    type: String,
  },
  city: {
    type: String,
  },
  state: {
    type: String,
  },
  pincode: {
    type: String,
  },
  avatar: {
    type: String,
  },
  preferences: {
    newsletter: Boolean,
    orderUpdates: Boolean,
    promotions: Boolean,
  },
});

module.exports = mongoose.model('UserProfile', UserProfileSchema);